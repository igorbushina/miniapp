if (countries.length > 0) {
  countrySelect.value = countries[0];
  const cities = (countryCityMap[countries[0]] || []).sort();
  cities.forEach((city) => {
    const option = document.createElement("option");
    option.value = city;
    option.text = city;
    citySelect.appendChild(option);
  });
}

